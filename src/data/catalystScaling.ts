export function catalystScalingEntryFor(name: string) {
  return catalystScaling.find((entry) => entry.name === name) ?? null;
}

export function catalystKindLabel(kind: CatalystScalingKind) {
  return kind === "sorcery" ? "Staff" : "Seal";
}

export function catalystRequirementLabel(entry: CatalystScalingEntry) {
  const requirements: string[] = [];
  if (entry.properMagic > 0) requirements.push(`${entry.properMagic} Int`);
  if (entry.properFaith > 0) requirements.push(`${entry.properFaith} Fai`);
  return requirements.length > 0 ? `Req ${requirements.join(" / ")}` : "";
}

export function catalystDisplayLabel(entry: CatalystScalingEntry) {
  const kind = catalystKindLabel(entry.kind);
  const requirementLabel = catalystRequirementLabel(entry);
  return requirementLabel ? `${entry.name} • ${kind} • ${requirementLabel}` : `${entry.name} • ${kind}`;
}
